    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Combustivel combustivel = new Combustivel();
        combustivel.setCodigo(Integer.parseInt(tfCodigo.getText().trim()));
        combustivel.setDescricao(tfDescricao.getText().trim());
        combustivel.setTipoCombustivel((String) cbTipo.getSelectedItem());
        try {
            combustivelDao.save(combustivel);
            JOptionPane.showMessageDialog(null, "Registro Salvo Com Sucesso!!!");
            limparCampos();
            desabilitaCampos(false);
            carregaTable(combustivelDao.getAll());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoActionPerformed
        try {
            tfCodigo.setText(String.valueOf(combustivelDao.getLastId()));
            btSalvar.setEnabled(true);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        desabilitaCampos(true);
        btSalvar.setEnabled(true);
    }//GEN-LAST:event_btNovoActionPerformed

    private void btFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFiltrarActionPerformed
        try {
            if (rbCodigo.isSelected() && tfCodigoFiltro.getText().trim().length() > 0) {
                List<Combustivel> combustivelFiltro = new ArrayList<>();
                combustivelFiltro.add(combustivelDao.getById(Integer.parseInt(tfCodigoFiltro.getText().trim())));
                carregaTable(combustivelFiltro);
            } else if (rbDescricao.isSelected() && tfDescricaoFiltro.getText().trim().length() > 0) {
                carregaTable(combustivelDao.getByName(tfDescricaoFiltro.getText().trim()));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btFiltrarActionPerformed

